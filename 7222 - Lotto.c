#include<stdio.h>
void DFS(int i);
int a[13]={0}, lo[13]={0}, num, k;

int main(void)
{
    int i;

    scanf("%d", &k);
    while(k)
    {
        for(i=1;i<=k;i++) scanf("%d", &a[i]);
        num=0;
        for(i=1;i<=k;i++) DFS(i);

        scanf("%d", &k);
        if(k) printf("\n");
    }

    return 0;
}

void DFS(int i)
{
    int j;
    lo[num++]=a[i];

    if(num==6)
    {
        for(j=0;j<6;j++) printf("%d%c", lo[j], (j<5)?' ':'\n');
        num--;
        return;
    }

    for(j=i+1;j<=k;j++) DFS(j);
    num--;
}

