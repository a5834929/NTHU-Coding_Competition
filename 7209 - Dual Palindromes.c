#include<stdio.h>

int main(void)
{
    int N, S, palin, num, check;
    int i, j, k, numo, count;

    while(scanf("%d %d", &N, &S)!=EOF)
    {
        numo=S+1;
        count=0;

        while(count<N)
        {
            int numb[100]={0};
            palin=0;


            for(i=2;i<=10;i++)
            {
                check=1;
                num=numo;
                j=0;
                while(num)
                {
                    numb[j]=num%i;
                    j++;
                    num/=i;
                }

                for(k=0;k<j;k++)
                {
                    if(numb[k]!=numb[j-1-k])
                    {
                        check=0;
                        break;
                    }

                }
                if(check) palin++;

                if(palin==2) break;

            }

            if(palin==2)
            {
                printf("%d\n", numo);
                count++;
            }
            numo++;
        }
    }
    return 0;
}

